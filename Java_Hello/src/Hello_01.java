
public class Hello_01 {

	
	// 시작점, Launcher
	public static void main(String[] args)  {
		// 요거 작성하는게 시험(*)
		
		// 큰 따옴표로 묶인 내용은 문자열 리터럴 이라고 하며
		//아래 코드는 문자열 리터럴을 console에 보여라 라는 코드
		System.out.println("Republic of Korea");
		System.out.println(" 30 + 40 ");
		System.out.println(" 30 * 40 ");	

	    // 그냥 (30+40) 하면 콘솔에 70표시되지만(*)
		// "30 + 40" 하면 30+40 눈에 보이는 그대로 콘솔에 표시 된다.(*)
		
		
		// 문자열 리터럴 Republic of Korea를 덧셈연산하여 
		// Console에 보여달라
		// 문자열 리터럴 Republic Of Korea를 연결하여
		// Console에 보여달라
		System.out.println("Republic" + "Of"+ "Korea");
		
		// 문자열 리터럴에 빈칸(space)가 포함되면 빈칸도 눈에 보이지는 않지만 빈칸도 하나의 문자열 리터럴이 된다.
		System.out.println("Republic " + "Of "+ "Korea");
		
	
	}
	
	
}
